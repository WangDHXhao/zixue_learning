####引用就是地址       #其实并不是开辟一个新的空间，而是指向一个内存地址
c = [11, 22]  ###c与d指向同一个地址，所以结果会改变
d = c
d.append(33)

print("c=", c, id(c))
print("d=", d, id(d))

